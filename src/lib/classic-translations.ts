export function localizeClassicCity(city: string, lang: "RU" | "KZ" | "EN"): string {
  if (lang === "RU") return city;
  return CLASSIC_CITY_I18N[city]?.[lang] ?? city;
}

export function localizeClassicRole(role: string, lang: "RU" | "KZ" | "EN"): string {
  if (lang === "RU") return role;
  return CLASSIC_ROLE_I18N[role]?.[lang] ?? role;
}

export function localizeClassicName(name: string, lang: "RU" | "KZ" | "EN"): string {
  const teamMatch = name.match(/^Команда\s+(.+)$/);
  if (!teamMatch || lang === "RU") return name;
  if (lang === "KZ") return `${teamMatch[1]} командасы`;
  return `${teamMatch[1]} team`;
}

export function localizeClassicAddress(address: string, lang: "RU" | "KZ" | "EN"): string {
  if (lang === "KZ") {
    return CLASSIC_ADDRESS_REPLACEMENTS_KZ.reduce(
      (current, [pattern, replacement]) => current.replace(pattern, replacement),
      address,
    );
  }
  if (lang === "EN") {
    return address.replace(/, Казахстан$/, ", Kazakhstan");
  }
  return address;
}
